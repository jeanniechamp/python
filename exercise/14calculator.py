x =input("Enter number 1: ")
y=input("Enter number 2: ")
o =input("Which operation? (+ , -, *, / ) ")
x=int(x)
y=int(y)
if o ("+"):
    print (x+y)
elif o ("-"):
    print (x-y)
elif o ("*"):
    print (x*y)
elif o ("/"):
    print (x/y)